import esbuild from "esbuild";
import process from "process";
import builtins from "builtin-modules";
import { copy } from "esbuild-plugin-copy";
import { watch } from "fs";
import path from "path";
import { execSync } from "child_process";
import { existsSync, mkdirSync } from "fs";

const banner =
`/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD
if you want to view the source, please visit the github repository of this plugin
*/
`;

const prod = process.env.NODE_ENV === "production";
const obsidianVaultPath = process.env.OBSIDIAN_VAULT_PATH;
const obsidianPluginPath = obsidianVaultPath ? path.join(obsidianVaultPath, '.obsidian', 'plugins', 'obsidian-lovpen') : process.env.OBSIDIAN_PLUGIN_PATH;

// è‡ªåŠ¨åŒæ­¥åˆ° Obsidian æ’ä»¶ç›®å½•çš„å‡½æ•°
const syncToObsidian = () => {
	if (obsidianPluginPath) {
		// æ£€æŸ¥ç›®æ ‡ç›®å½•æ˜¯å¦å­˜åœ¨ï¼Œä¸å­˜åœ¨åˆ™åˆ›å»º
		if (!existsSync(obsidianPluginPath)) {
			console.log(`ğŸ“ Creating Obsidian plugin directory: ${obsidianPluginPath}`);
			try {
				mkdirSync(obsidianPluginPath, { recursive: true });
				console.log(`âœ… Directory created successfully`);
			} catch (error) {
				console.error(`âŒ Failed to create directory: ${error.message}`);
				process.exit(1);
			}
		}
		
		try {
			execSync(`rsync -a packages/obsidian/dist/ "${obsidianPluginPath}"`, { 
				stdio: 'inherit',
				cwd: path.resolve('../..')
			});
			console.log(`ğŸ”„ Synced to Obsidian plugin directory: ${obsidianPluginPath}`);
		} catch (error) {
			console.error('âŒ Failed to sync to Obsidian:', error.message);
			process.exit(1);
		}
	}
};

const context = await esbuild.context({
	banner: {
		js: banner,
	},
	entryPoints: ["main.ts"],
	bundle: true,
	external: [
		"obsidian",
		"electron",
		"@codemirror/autocomplete",
		"@codemirror/collab",
		"@codemirror/commands",
		"@codemirror/language",
		"@codemirror/lint",
		"@codemirror/search",
		"@codemirror/state",
		"@codemirror/view",
		"@lezer/common",
		"@lezer/highlight",
		"@lezer/lr",
		...builtins],
	format: "cjs",
	target: "es2018",
	logLevel: "info",
	sourcemap: prod ? false : "inline",
	treeShaking: true,
	outfile: "dist/main.js",
	plugins: [
		copy({
			// å¤åˆ¶ä»¥ä¸‹èµ„æºåˆ° dist/assets ç›®å½•
			assets: [
				// å¤åˆ¶æ’ä»¶æ‰€éœ€çš„å…¶ä»–æ–‡ä»¶åˆ° dist ç›®å½•
				{ from: ['./manifest.json'], to: ['./manifest.json'], outDir: './dist' },
				{ from: ['../assets/**/*'], to: ['./assets/'], outDir: './dist' },
				{ from: ['../frontend/dist/**/*'], to: ['./frontend/'], outDir: './dist' },
			],
			verbose: false, // è¾“å‡ºå¤åˆ¶æ“ä½œçš„æ—¥å¿—
		}),
	],
});



if (prod) {
	await context.rebuild();
	syncToObsidian();
	process.exit(0);
} else {
	await context.watch();
	
	// ç›‘å¬frontend distç›®å½•çš„å˜åŒ–ï¼Œè§¦å‘é‡æ–°æ„å»º
	const frontendDistPath = path.resolve('../frontend/dist');
	console.log(`ğŸ” Watching frontend dist: ${frontendDistPath}`);
	
	let rebuildTimeout;
	const debounceRebuild = () => {
		clearTimeout(rebuildTimeout);
		rebuildTimeout = setTimeout(async () => {
			try {
				console.log('ğŸ”„ Frontend assets changed, rebuilding...');
				await context.rebuild();
				console.log('âœ… Rebuild completed');
				syncToObsidian();
			} catch (error) {
				console.error('âŒ Rebuild failed:', error);
			}
		}, 100); // 100msé˜²æŠ–
	};
	
	try {
		watch(frontendDistPath, { recursive: true }, (eventType, filename) => {
			if (filename) {
				console.log(`ğŸ“ Frontend file changed: ${filename} (${eventType})`);
				debounceRebuild();
			}
		});
		
		console.log('ğŸ‘€ Watching for changes...');
	} catch (error) {
		console.warn('âš ï¸ Could not watch frontend dist directory:', error.message);
		console.log('ğŸ’¡ Make sure to run "pnpm dev" in the frontend package first');
	}
}
